    # It's an oversized crayon.
    # The literal crayon covers the low left to right.
    # We will rotate it for the isometric style in a second step.
    img = draw_custom_pixel_template(t, {
        '.': (0,0,0,0), 'O': (30,20,20,255), 'X': (150,40,40,255), '=': (200,60,60,255), 
        '#': (240,100,100,255), 'S': (255,80,80,255) # scribble
    })
    
    # Special fix for the childs drawing: re-center after rotation
    img_rot = img.rotate(45, resample=Image.NEAREST, expand=True)
    bbox = img_rot.getbbox()
    if bbox:
        img_rot = img_rot.crop(bbox)
        img_rot = img_rot.resize((60, 60), Image.NEAREST) # Make sure it fits
        
    final_img = Image.new("RGBA", (64, 64), (0, 0, 0, 0))
    final_img.paste(img_rot, ((64 - img_rot.width) // 2, (64 - img_rot.height) // 2))
    return final_img

