def get_material_palette(material):
    if material == "bois":
        return {"base": (90, 65, 40, 255), "dark": (50, 35, 20, 255), "light": (130, 95, 55, 255), "edge": (160, 130, 90, 255), "grip": (60, 40, 30, 255)}
    elif material == "pierre":
        return {"base": (120, 120, 125, 255), "dark": (70, 70, 75, 255), "light": (160, 160, 170, 255), "edge": (200, 200, 210, 255), "grip": (80, 60, 40, 255)}
    elif material == "metal":
        return {"base": (110, 120, 130, 255), "dark": (60, 70, 80, 255), "light": (180, 190, 200, 255), "edge": (230, 240, 255, 255), "grip": (40, 30, 25, 255)}
    elif material == "essence":
        return {"base": (120, 40, 180, 255), "dark": (60, 10, 90, 255), "light": (190, 90, 255, 255), "edge": (240, 180, 255, 255), "grip": (20, 20, 30, 255)}
    return {"base": (128, 128, 128, 255), "dark": (64, 64, 64, 255), "light": (192, 192, 192, 255), "edge": (255, 255, 255, 255), "grip": (0,0,0,255)}

def draw_custom_pixel_template(template_str, custom_mapping, size=64):
    lines = template_str.strip("\n").split("\n")
    h = len(lines)
    w = max(len(line) for line in lines)
    
    img = Image.new("RGBA", (w, h), (0, 0, 0, 0))
    for y, line in enumerate(lines):
        for x, char in enumerate(line):
            if char in custom_mapping:
                c = custom_mapping[char]
                # Slight randomization for organic texture if it's not transparent/outline
                if char not in ['.', 'O'] and c[3] == 255 and random.random() < 0.15:
                    c = (max(0, min(255, c[0] + random.randint(-10, 10))),
                         max(0, min(255, c[1] + random.randint(-10, 10))),
                         max(0, min(255, c[2] + random.randint(-10, 10))), c[3])
                img.putpixel((x, y), c)
                
    # 1. Crop to non-transparent bounding box to remove excess ASCII padding
    bbox = img.getbbox()
    if bbox:
        img = img.crop(bbox)
        
    # 2. Scale up so the largest dimension fits the target size (with a 2px padding margin)
    margin = 4
    target_inner_size = size - margin * 2
    
    scale_factor = target_inner_size / max(img.width, img.height)
    new_w = int(img.width * scale_factor)
    new_h = int(img.height * scale_factor)
    
    # Force Nearest Neighbor to maintain pixel art
    img = img.resize((new_w, new_h), Image.NEAREST)
    
    # 3. Paste centered on exactly a size x size transparent canvas
    final_img = Image.new("RGBA", (size, size), (0, 0, 0, 0))
    x_offset = (size - new_w) // 2
    y_offset = (size - new_h) // 2
    
    final_img.paste(img, (x_offset, y_offset))
    return final_img

def draw_pixel_template(template_str, mat):
    pal = get_material_palette(mat)
    outline = (25, 20, 25, 255) if mat != "essence" else (30, 5, 45, 255)
    accent = (160, 40, 40, 255) if mat in ["bois", "metal"] else ((100, 255, 150, 255) if mat == "essence" else (100, 120, 100, 255))
    
    mapping = {
        '.': (0, 0, 0, 0),
        'O': outline,
        'X': pal["dark"],
        '=': pal["base"],
        '#': pal["light"],
        'E': pal["edge"],
        'G': pal["grip"],
        'A': accent
    }
    return draw_custom_pixel_template(template_str, mapping)

